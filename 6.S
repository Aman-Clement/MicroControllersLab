	AREA PROG6,CODE,READONLY
RADIX RN 0
UPPERNIBBLE RN 1
LOWERNIBBLE RN 2
UPPERNIBBLEMASK RN 3
LOWERNIBBLEMASK RN 4
NUMBYTES RN 5
BYTE RN 6
RESULT RN 7
ENTRY
	MOV RADIX,#10
	MOV UPPERNIBBLEMASK,#0XF0
	MOV LOWERNIBBLEMASK,#0X0F
	MOV RESULT,#0
	MOV NUMBYTES,#4
	LDR R8,=NUMBER
	ADD R8,R8,NUMBYTES
	SUB R8,R8,#1
	MOV R9,#0X40000000
LOOP
	LDRB BYTE,[R8]
	SUB R8,R8,#1
	AND UPPERNIBBLE,UPPERNIBBLEMASK,BYTE
	AND LOWERNIBBLE,LOWERNIBBLEMASK,BYTE
	LSR UPPERNIBBLE,#4
	MLA RESULT,RADIX,RESULT,UPPERNIBBLE
	MLA RESULT,RADIX,RESULT,LOWERNIBBLE
	SUBS NUMBYTES,#1
	BNE LOOP
	STR R7,[R9]
GO B GO
NUMBER DCD 0X00000127
	END