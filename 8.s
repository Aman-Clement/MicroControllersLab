	AREA PROG8,CODE,READONLY
DIVIDEND RN 1
DIVISOR RN 2
QUOTIENT RN 3
N RN 4
R RN 5
NDR RN 6
ENTRY
	MOV N,#6
	MOV R,#3
	MOV R9,#0X40000000
	SUB NDR,N,R
	MOV DIVIDEND,N
	BL FACT
	MOV N,R7
	MOV DIVIDEND,NDR
	BL FACT
	MOV NDR,R7
	MOV DIVIDEND,R
	BL FACT
	MOV R,R7
	MOV DIVIDEND,N
	MUL DIVISOR,NDR,R
	BL DIV
	STR QUOTIENT,[R9]
STOP B STOP

FACT
	MOV R7,#1
LOOP1 MUL R7,DIVIDEND,R7
	SUBS DIVIDEND,#1
	BNE LOOP1
	BX LR
	
DIV
	MOV QUOTIENT,#0
LOOP2 SUBS DIVIDEND,DIVIDEND,DIVISOR
	ADDPL QUOTIENT,#1
	BPL LOOP2
	ADDMI R8,DIVIDEND,DIVISOR
	BX LR
	END